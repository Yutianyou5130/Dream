extern bool Positive(int positive)
{
    return positive > 0;
}

extern bool None_Positive(int none_Positive)
{
    return none_Positive <= 0;
}

extern bool Negative(int negative)
{
    return negative < 0;
}

extern bool None_Negative(int none_Negative)
{
    return none_Negative >= 0;
}